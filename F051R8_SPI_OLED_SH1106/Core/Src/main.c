void Update_Main_Screen() {
  SH1106_Clear();
  SH1106_SetMask(0, 0, SH1106_WIDTH, SH1106_HEIGHT);

  // draw an animation frame
  SH1106_DrawBitmap(0, 0, frames[b%10], 80, 32, SH1106_WHITE, SH1106_TRANSPARENT);
  b++;

  SH1106_DrawFilledCircle(cx, cy, cr, SH1106_WHITE);
  cx+=cxd;
  if(cx<0 || cx>SH1106_WIDTH) cxd=-cxd;
  cy+=cyd;
  if(cy<0 || cy>SH1106_HEIGHT) cyd=-cyd;

  // draw frame rate
  sprintf(message, "%d", SH1106.FPS);
  SH1106_WriteString(m, 0, message, &Font_11x18, SH1106_WHITE, SH1106_TRANSPARENT);

  w = Font_GetStringWidth(message, &Font_11x18);
  SH1106_WriteString(m+w, 0, fps, &Font_7x10, SH1106_WHITE, SH1106_TRANSPARENT);

  // draw a scrolling text
  sprintf(message, "%2d scrolling", b/10);
  h = Font_GetStringHeight(message, &Font_11x18)+4;
  SH1106_SetMask(m, h, SH1106_WIDTH, h+Font_7x10.height); // text is only visible inside this mask
  SH1106_WriteString(x, h, message, &Font_7x10, SH1106_WHITE, SH1106_TRANSPARENT);

  // scroll text
  w = Font_GetStringWidth(message, &Font_7x10);
  // effect: pause scrolling in one second
  if(x==m+10) {
    s++;
    if (s > SH1106.FPS) {
      s = 0;
      x--;
    }
  } else {
    x--;
    if(x < m-w) x = SH1106_WIDTH;
  }
}

